export const languageOptions = Object.keys(translations);

export function getText(language, key) {
  return translations[language]?.[key] ?? translations.en[key] ?? key;
}

export function applyStaticTranslations(language) {
  const nodes = document.querySelectorAll("[data-i18n]");
  nodes.forEach((node) => {
    const key = node.getAttribute("data-i18n");
    node.textContent = getText(language, key);
  });

  const placeholders = document.querySelectorAll("[data-i18n-placeholder]");
  placeholders.forEach((node) => {
    const key = node.getAttribute("data-i18n-placeholder");
    node.placeholder = getText(language, key);
  });
}
